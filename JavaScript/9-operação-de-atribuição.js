/*

    Operação de Atribuição em JavaScript

    = -> Atribuição
    += -> Atribuição com adição
    -= -> Atribuição com subtração
    *= -> Atribuição com multiplicação
    /= -> Atribuição com divisão

*/

let x = 10; // Atribuição
console.log('Valor inicial de x:', x);

x += 5; // Atribuição com adição
console.log('Atribuição com adição:', x);

x -= 3; // Atribuição com subtração
console.log('Atribuição com subtração:', x);

x *= 2; // Atribuição com multiplicação
console.log('Atribuição com multiplicação:', x);

x /= 4; // Atribuição com divisão
console.log('Atribuição com divisão:', x);