/*

    Operadores Aritméticos

    +  -> Adição / Concatenação
    -  -> Subtração
    *  -> Multiplicação
    /  -> Divisão
    %  -> Resto da divisão
    ++ -> Incremento
    -- -> Decremento
    ** -> Exponenciação

*/

let num1 = 10;
let num2 = 5;

// Adição
const addition = num1 + num2;
console.log('Adição:', addition);

// Subtração
const subtraction = num1 - num2;
console.log('Subtração:', subtraction);

// Multiplicação
const multiplication = num1 * num2;
console.log('Multiplicação:', multiplication);

// Divisão
const division = num1 / num2;
console.log('Divisão:', division);

// Resto da divisão
const remainder = num1 % num2;
console.log('Resto da divisão:', remainder);  

// Incremento
num1++;
console.log('Incremento:', num1);

// Decremento
num2--;
console.log('Decremento:', num2);

// Exponenciação
const exponentiation = num1 ** num2;
console.log('Exponenciação:', exponentiation);