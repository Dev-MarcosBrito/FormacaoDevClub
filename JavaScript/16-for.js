/*

    For Loop

    1. Estrutura básica:

    - Inicialização: Define uma variável de controle e seu valor inicial.
    - Condição: Verifica se a variável de controle atende a uma condição para continuar o loop.
    - Expressão final: Atualiza a variável de controle após cada iteração.


*/

