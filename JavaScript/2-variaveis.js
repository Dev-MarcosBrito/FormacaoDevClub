/*
    Variáveis em JavaScript
        
    -LET -> Pode ser alterada
    -CONST -> Não pode ser alterada
    -VAR -> Não utilizar
*/

let numeroDeAlunosDaSalaSete = 40;
console.log(numeroDeAlunosDaSalaSete);


const pi = 3.14;
// pi = 3.15; // Isso gera um erro, pois CONST não pode ser alterada
console.log(pi);

var idade = 25; // Não utilizar VAR
console.log(idade);
