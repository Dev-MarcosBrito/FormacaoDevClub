/*

Math -> Funções matemáticas
    -pow -> Potência
    -sqrt -> Raiz quadrada
    -abs -> Valor absoluto
    -ceil -> Arredonda para cima
    -floor -> Arredonda para baixo
    -PI -> 3.14
    -random -> Número aleatório 

*/

const result = Math.pow(2, 3); // 2 elevado a 3
console.log(result);

const result2 = Math.sqrt(9); // Raiz quadrada de 9
console.log(result2);

const result3 = Math.abs(-10); // Valor absoluto de -10
console.log(result3);

const result4 = Math.ceil(10.1); // Arredonda para cima
console.log(result4);

const result5 = Math.floor(10.9); // Arredonda para baixo
console.log(result5);

const result6 = Math.PI; // 3.14
console.log(result6);

const result7 = Math.random(); // Número aleatório
console.log(result7);