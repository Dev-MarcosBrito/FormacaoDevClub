/*

    Operação de comparação

    >  Maior que: >
    >  Menor que: <
    >  Maior ou igual a: >=
    >  Menor ou igual a: <=
    >  Igual a: ==
    >  Diferente de: !=

*/

const firstNumber = 10
const secondNumber = 5

if (firstNumber > secondNumber) {
    console.log(`${firstNumber} é maior que ${secondNumber}`)
}

if (firstNumber < secondNumber) {
    console.log(`${firstNumber} é menor que ${secondNumber}`)
}

if (firstNumber >= secondNumber) {
    console.log(`${firstNumber} é maior ou igual a ${secondNumber}`)
}

if (firstNumber <= secondNumber) {
    console.log(`${firstNumber} é menor ou igual a ${secondNumber}`)
}